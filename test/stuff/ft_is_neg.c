#include <unistd.h>

void	numnb(int n)
{
	write (1, (n >= 0) ? "P" : "N", 1);
}

int	main(void)
{
	numnb(1);
	write (1, "\n", 1);
	numnb(0);
	write (1, "\n", 1);
	numnb(-1);
}