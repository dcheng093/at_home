#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

int	main(void)
{
	int a;
	int b;

	a = 25;
	b = 5;
	ft_swap(&a, &b);
	printf("a = %d\nb = %d\n", a, b);
}