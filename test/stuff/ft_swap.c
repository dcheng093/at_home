#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int	temp;
	temp = *a;
	*a = *b;
	*b = temp;
}

int	main(void)
{
	int	a = 4;
	int b = 2;

	ft_swap(&a, &b);
	printf("%d\n", a);
}